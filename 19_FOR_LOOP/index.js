// This loop will print numbers from 0 to 4

for (let i = 0; i < 5; i++) {
  // Initialization:  (let i = 0;)  Runs once at the start. Sets up a counter 'i'.
  // Condition:       (i < 5;)     Checks this before each loop. If true, the loop continues.
  // Increment:       (i++)        Runs after each loop iteration. Increases 'i' by 1.

  console.log("Number: " + i);
}

// Output:
// Number: 0
// Number: 1
// Number: 2
// Number: 3
// Number: 4